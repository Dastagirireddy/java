package com.javaexample;

public class Box2 
{
	void test()
	{
		System.out.println("No Parameter.!!");
	}
	void test(int a)
	{
		System.out.println("The Integer Parameter:" +a);
	}
	void test(int a,int b)
	{
		System.out.println("Two Integer Parameter:"+ a +"and"+b);
	}
	double test(double a)
	{
		return a*a;
	}
}
